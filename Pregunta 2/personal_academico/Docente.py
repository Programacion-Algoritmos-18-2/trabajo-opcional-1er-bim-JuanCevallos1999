class Docente():

	def __init__(self):
		self.nombre = " "
		self.apellido = " "
		self.titulo = " "

	def set_nombre(self, n):
		self.nombre = n
	
	def get_nombre(self):
		return self.nombre

	def set_apellido(self, a):
		self.apellido = a
	
	def get_apellido(self):
		return self.apellido

	def set_titulo(self, n):
		self.titulo = n
	
	def get_titulo(self):
		return self.titulo
